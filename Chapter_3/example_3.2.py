
potion_ingredients = ["Moonpetal", "Dragon's Scale", "Phoenix Feather", "Unicorn Horn"]

# Accessing the first ingredient (index 0)
first_ingredient = potion_ingredients[0]
print(f"First ingredient: {first_ingredient}") # Output: First ingredient: Moonpetal

# Accessing the third ingredient (index 2)
third_ingredient = potion_ingredients[2]
print(f"Third ingredient: {third_ingredient}") # Output: Third ingredient: Phoenix Feather

# Accessing the last ingredient using negative indexing
# -1 refers to the last item, -2 to the second to last, etc.
last_ingredient = potion_ingredients[-1] 
print(f"Last ingredient: {last_ingredient}") # Output: Last ingredient: Unicorn Horn
