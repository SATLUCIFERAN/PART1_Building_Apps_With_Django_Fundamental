

# Coordinates of a hidden ancient ruin (fixed once discovered)
ruin_coordinates = (100, 250, 75) # (x, y, z altitude)
print(f"Ruin Coordinates: {ruin_coordinates}") 
# Output: Ruin Coordinates: (100, 250, 75)

# A tuple representing a fixed elemental combination
elemental_trio = ("Fire", "Water", "Earth")
print(f"Elemental Trio: {elemental_trio}") 
# Output: Elemental Trio: ('Fire', 'Water', 'Earth')

# A single-item tuple (note the comma!)
single_prophecy = ("The Chosen One",)
print(f"Single Prophecy: {single_prophecy}") 
# Output: Single Prophecy: ('The Chosen One',)



