
# Examples of different data types
spell_name = "Invisibility Cloak"  # str
spell_power = 100                 # int
mana_cost = 15.5                  # float
is_enchanted = True               # bool

# You can check the type of a variable using the type() function
print(type(spell_name))
print(type(spell_power))
print(type(mana_cost))
print(type(is_enchanted))
