# This is a comment - Python ignores lines starting with #

# Creating a variable named 'apprentice_name' and storing text in it
apprentice_name = "Elara"

# Creating a variable named 'lab_number' and storing a whole number in it
lab_number = 7

# Creating a variable named 'is_magic_active' and storing a True/False value
is_magic_active = True

# You can use variables in your print statements
print("Welcome, " + apprentice_name + "!")
print("You are in Lab number:", lab_number)
print("Is magic active?", is_magic_active)

# You can change the value stored in a variable
apprentice_name = "Kael"
print("Now, the apprentice is: " + apprentice_name)